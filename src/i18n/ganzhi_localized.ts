/** Look up a stem character; returns the active language's cell or undefined. */
export function getStemCell(
  stemChar: string,
  lang: LanguageCode
): LocalizedCell | undefined {
  return STEM_TRANSLATIONS[stemChar]?.[lang];
}

/** Look up a branch character; returns the active language's cell or undefined. */
export function getBranchCell(
  branchChar: string,
  lang: LanguageCode
): LocalizedCell | undefined {
  return BRANCH_TRANSLATIONS[branchChar]?.[lang];
}

/**
 * Format a 2-char ganzhi (e.g. "甲子") into the active language's script
 * slot — the equivalent of rendering the original Hanzi in the language's
 * native script. Falls back to the original Hanzi when the language has
 * no entry for one of the chars.
 */
export function formatGanZhiScript(
  ganzhi: string | null | undefined,
  lang: LanguageCode
): string {
  if (!ganzhi || ganzhi.length < 2) return ganzhi ?? "";
  const stem = ganzhi[0]!;
  const branch = ganzhi[1]!;
  const stemCell = getStemCell(stem, lang);
  const branchCell = getBranchCell(branch, lang);
  return (stemCell?.script ?? stem) + (branchCell?.script ?? branch);
}

/**
 * Format a 2-char ganzhi into the active language's romanization slot.
 * Always returns "<stem-roman> <branch-roman>" with a space between.
 */
export function formatGanZhiRoman(
  ganzhi: string | null | undefined,
  lang: LanguageCode
): string {
  if (!ganzhi || ganzhi.length < 2) return "";
  const stem = ganzhi[0]!;
  const branch = ganzhi[1]!;
  const stemCell = getStemCell(stem, lang);
  const branchCell = getBranchCell(branch, lang);
  if (!stemCell?.roman || !branchCell?.roman) return "";
  return `${stemCell.roman} ${branchCell.roman}`;
}
